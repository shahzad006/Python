x = 5


x = 5     # Assign value
x += 3    # x = x + 3 → 8
x -= 2    # x = x - 2 → 6
x *= 4    # x = x * 4 → 24
x /= 6    # x = x / 6 → 4.0
x %= 3    # x = x % 3 → 1.0
x //= 2   # x = x // 2 → 0.0
x **= 3   # x = x ** 3 → 0.0


print(x)  